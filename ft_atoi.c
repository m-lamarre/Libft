#include "libft.h"

int		ft_atoi(const char *str)
{
	int		ans;
	int		n;

	ans = 0;
	while ((*str >= '\t' && *str <= '\r') || *str == 32)
		str++;
	if (*str == '-')
		n = -1;
	else
		n = 1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str && ft_isdigit(*str))
	{
		ans = (ans * 10) + (*str - '0');
		str++;
	}
	return (ans * n);
}
