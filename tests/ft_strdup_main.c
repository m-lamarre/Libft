#include <stdlib.h>
#include <stdio.h>

char	*ft_strcpy(char *dst, const char *src)
{
    int	i;

    i = 0;
    while (src[i] != '\0')
    {
        dst[i] = src[i];
        i++;
    }
    dst[i] = '\0';
    return (dst);
}

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(const char *s1)
{
	char	*s2;

	s2 = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (s2 == NULL)
		return (NULL);
	ft_strcpy(s2, s1);
	return (s2);
}

int main(void)
{
	char *s = "This is the first string entered";
	char *newstr;
	if ((newstr = ft_strdup(s)) != NULL)
		printf("The new string is: %s\n", newstr);
	return 0;
}
